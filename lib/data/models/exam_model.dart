import 'package:equatable/equatable.dart';

class Exam extends Equatable {
  final int id;
  final String title;
  final String type; // 'mcq', 'cq', 'mixed'
  final String date;
  final String status; // 'attempted', 'new'
  final int score;
  final int total;

  const Exam({
    required this.id,
    required this.title,
    required this.type,
    required this.date,
    required this.status,
    required this.score,
    required this.total,
  });

  @override
  List<Object?> get props => [];
}
